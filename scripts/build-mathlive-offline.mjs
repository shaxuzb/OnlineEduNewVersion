// Generates `src/assets/mathlive/mathliveAssets.ts` with MathLive bundled
// FULLY OFFLINE: the engine JS + the static CSS with every KaTeX font embedded
// as a base64 data URI. This removes the runtime CDN dependency in the math
// keyboard (react-native-webview), so the keyboard opens instantly and keeps
// working with no internet — no more network stall/freeze.
//
// Pinned to a version whose API matches the WebView code in
// `src/components/MathKeyboard.tsx` (uses `mf.setOptions(...)` and
// `mf.mathVirtualKeyboard` — classic MathLive ~0.9x API).
//
// Re-generate (needs internet, only at build time — never at runtime):
//   node scripts/build-mathlive-offline.mjs

import { writeFileSync, mkdirSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const VERSION = "0.98.6";
const CDN = `https://cdn.jsdelivr.net/npm/mathlive@${VERSION}/dist`;

const __dirname = dirname(fileURLToPath(import.meta.url));
const OUT = join(
  __dirname,
  "..",
  "src",
  "assets",
  "mathlive",
  "mathliveAssets.ts",
);

async function fetchText(url) {
  const res = await fetch(url);
  if (!res.ok) throw new Error(`HTTP ${res.status} for ${url}`);
  return res.text();
}

async function fetchBuffer(url) {
  const res = await fetch(url);
  if (!res.ok) throw new Error(`HTTP ${res.status} for ${url}`);
  return Buffer.from(await res.arrayBuffer());
}

async function main() {
  console.log(`Fetching MathLive ${VERSION} from CDN…`);
  const js = await fetchText(`${CDN}/mathlive.min.js`);
  let css = await fetchText(`${CDN}/mathlive-static.css`);

  // Inline every referenced woff2 font as a data URI so the CSS is self-contained.
  const fontFiles = [
    ...new Set(
      [...css.matchAll(/fonts\/([A-Za-z0-9._-]+\.woff2)/g)].map((m) => m[1]),
    ),
  ];
  console.log(`Embedding ${fontFiles.length} fonts…`);
  for (const file of fontFiles) {
    const buf = await fetchBuffer(`${CDN}/fonts/${file}`);
    const dataUri = `data:font/woff2;base64,${buf.toString("base64")}`;
    css = css.split(`fonts/${file}`).join(dataUri);
  }

  const banner =
    `// AUTO-GENERATED by scripts/build-mathlive-offline.mjs — DO NOT EDIT BY HAND.\n` +
    `// MathLive ${VERSION} bundled offline (engine + CSS + ${fontFiles.length} KaTeX fonts as base64).\n` +
    `// Re-generate: node scripts/build-mathlive-offline.mjs\n`;

  const contents =
    banner +
    `export const MATHLIVE_VERSION = ${JSON.stringify(VERSION)};\n` +
    `export const MATHLIVE_JS = ${JSON.stringify(js)};\n` +
    `export const MATHLIVE_CSS = ${JSON.stringify(css)};\n`;

  mkdirSync(dirname(OUT), { recursive: true });
  writeFileSync(OUT, contents);

  console.log(`\n✅ Wrote ${OUT}`);
  console.log(`   JS:  ${(js.length / 1024).toFixed(0)} KB`);
  console.log(
    `   CSS: ${(css.length / 1024).toFixed(0)} KB (with ${fontFiles.length} embedded fonts)`,
  );
}

main().catch((err) => {
  console.error("❌ build-mathlive-offline failed:", err.message);
  process.exit(1);
});
